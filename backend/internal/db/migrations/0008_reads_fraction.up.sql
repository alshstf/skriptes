-- Прогресс чтения (0.0–1.0) — для отображения «Продолжить N%» на
-- кнопке открытия ридера в карточке книги. Foliate-js эмитит fraction
-- на каждом relocate-event'е; раньше мы его игнорировали (сохраняли
-- только last_pos = epub-cfi). Теперь сохраняем оба значения.
--
-- REAL (single-precision float) хватает: точность до 1/10000 для
-- значений [0,1] = 0.01%, юзеру округляем до целого процента.
--
-- Колонка nullable: для существующих записей без сохранённой позиции
-- значение неизвестно (last_pos тоже NULL в таких случаях), и без
-- ридера её никто не заполнит. NULL → UI показывает «Читать» без %.

ALTER TABLE reads ADD COLUMN fraction REAL;
