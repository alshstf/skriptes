-- Нормализуем коды языков к нижнему регистру + trim. В источниках (INPX/fb2)
-- один и тот же язык встречается в разном регистре (например 'ru' и 'RU'), из-за
-- чего язык двоился в списке/фильтре и в настройках видимости контента: скрыть
-- «Русский (RU)» и «Русский (ru)» приходилось по отдельности. lower(btrim(...))
-- схлопывает варианты в канонический ISO-639-1 код.
--
-- Meili-индекс синкается отдельно (one-shot ResyncLangs на старте по флагу
-- app_settings.lang_normalized_v1), т.к. SQL-миграция бэкенда Meili не трогает.
UPDATE books
SET lang = lower(btrim(lang))
WHERE lang IS NOT NULL AND lang <> lower(btrim(lang));
