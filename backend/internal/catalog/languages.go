// LanguageDisplay возвращает отображаемое имя языка по коду. Экспортируется,
// чтобы тот же маппинг переиспользовали другие места (фасеты/чипы).
func LanguageDisplay(code string) string {
	if name, ok := langNames[strings.ToLower(code)]; ok {
		return name
	}
	return strings.ToUpper(code)
}

// ListLanguages — все языки, встречающиеся в живых (не удалённых) книгах,
// с числом книг. Отсортированы по убыванию количества (популярные сверху),
// tiebreaker — код. Полный список (без фильтрации по скрытым) — он нужен
// разделам «Контент», где скрытые языки как раз нужно показать, чтобы их
// можно было включить обратно.
func (s *Service) ListLanguages(ctx context.Context) ([]LanguageEntry, error) {
	// Группируем по НОРМАЛИЗОВАННОМУ коду (lower+trim): defensive-дедуп, чтобы
	// один язык в разном регистре ('ru'/'RU') не двоился в списке, даже если в
	// данные просочился ненормализованный вариант (импорт их чистит, плюс
	// миграция 0015 — но запрос не должен зависеть от чистоты данных).
	rows, err := s.pool.Query(ctx, `
		SELECT lower(btrim(lang)) AS code, count(*)::int
		FROM books
		WHERE deleted = false AND lang IS NOT NULL AND btrim(lang) <> ''
		GROUP BY lower(btrim(lang))
		ORDER BY count(*) DESC, code
	`)
	if err != nil {
		return nil, fmt.Errorf("list languages: %w", err)
	}
	defer rows.Close()

	out := make([]LanguageEntry, 0, 16)
	for rows.Next() {
		var e LanguageEntry
		if err := rows.Scan(&e.Code, &e.BookCount); err != nil {
			return nil, fmt.Errorf("scan language: %w", err)
		}
		e.Display = LanguageDisplay(e.Code)
		out = append(out, e)
	}
	if err := rows.Err(); err != nil {
		return nil, err
	}
	return out, nil
}

// ListSrcLanguages — все ЭФФЕКТИВНЫЕ ЯЗЫКИ ОРИГИНАЛА живых книг с числом книг:
// src_lang (fb2 <src-lang>), а где пусто — язык издания (натив сам себе
// оригинал). Опции фильтра «Язык оригинала» на /books и в «Авторах» — на том же
// поле, что works-индексный фасет orig_lang. Так «оригинал: французский» ловит и
// переводы с французского, и нативно-французские книги; и любой язык коллекции
// (включая русский для «всей русской литературы») доступен как опция.
func (s *Service) ListSrcLanguages(ctx context.Context) ([]LanguageEntry, error) {
	rows, err := s.pool.Query(ctx, `
		SELECT lower(btrim(COALESCE(NULLIF(btrim(src_lang), ''), lang))) AS code, count(*)::int
		FROM books
		WHERE deleted = false
		  AND COALESCE(NULLIF(btrim(src_lang), ''), lang) IS NOT NULL
		  AND btrim(COALESCE(NULLIF(btrim(src_lang), ''), lang)) <> ''
		GROUP BY lower(btrim(COALESCE(NULLIF(btrim(src_lang), ''), lang)))
		ORDER BY count(*) DESC, code
	`)
	if err != nil {
		return nil, fmt.Errorf("list src languages: %w", err)
	}
	defer rows.Close()

	out := make([]LanguageEntry, 0, 8)
	for rows.Next() {
		var e LanguageEntry
		if err := rows.Scan(&e.Code, &e.BookCount); err != nil {
			return nil, fmt.Errorf("scan src language: %w", err)
		}
		e.Display = LanguageDisplay(e.Code)
		out = append(out, e)
	}
	if err := rows.Err(); err != nil {
		return nil, err
	}
	return out, nil
}
